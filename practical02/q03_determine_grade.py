#Determining grade

#main

#input
s=int(input("Enter score: "))

#output
if s>=70 and s<=100:
   print("A")
if s>=60 and s<=69:
   print("B")
if s>=55 and s<=59:
   print("C")
if s>=50 and s<=54:
   print("D")
if s>=45 and s<=49:
   print("E")
if s>=35 and s<=44:
   print("S")
if s>=0 and s<=34:
   print("U")
